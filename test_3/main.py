#Угадай число
n1, n2 = 1, 100
while True:
    x = (n1+n2)//2
    print('Твое число больше, меньше или равно', x,'?')
    n = (input('< - Больше, > - Меньше, = - Равно:  '))
    if n == '>':
        n2 = x
    elif n == '<':
        n1 = x
    elif n == '=':
        print('Я угадал')
        break
    else:
        print('Введен не верный символ')